    private void UpdateButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButActionPerformed

        int i = LectTB.getSelectedRow();
        DefaultTableModel tb = (DefaultTableModel) LectTB.getModel();
        if(i>=0){
            tb.setValueAt(NameTxt.getText(), i, 0);
            tb.setValueAt(LectIDTxt.getText(), i, 1);
            tb.setValueAt(DepTxt.getText(), i, 2);
            tb.setValueAt(EmailTxt.getText(), i, 3);
            tb.setValueAt(PasswordTxt.getText(), i, 4);
        }
        else{
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_UpdateButActionPerformed

