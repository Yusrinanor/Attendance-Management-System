    private void SaveButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButActionPerformed
        try {
            FileWriter outputfile = new FileWriter("studentrecord.txt");
            BufferedWriter bw = new BufferedWriter(outputfile);

            for (int i = 0; i < tm.getRowCount(); i++) {
                for (int j = 0; j < tm.getColumnCount(); j++) {
                    bw.write((String) tm.getValueAt(i, j) + ";");
                }

                bw.write("");
                bw.newLine();
            }
            bw.close();
            outputfile.close();
            JOptionPane.showMessageDialog(this, "Changes is saved");
             NameTxt.setText("");
             StudIDTxt.setText("");
             IntakeTxt.setText("");
             CourseTxt.setText("");
             PasswordTxt.setText("");

        } catch (IOException ex) {
            Logger.getLogger(ViewStudentRec.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SaveButActionPerformed

    private void UpdateButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButActionPerformed
    
        int i = StudTB.getSelectedRow();
        if(i>=0){
            tm.setValueAt(NameTxt.getText(), i, 0);
            tm.setValueAt(StudIDTxt.getText(), i, 1);
            tm.setValueAt(IntakeTxt.getText(), i, 2);
            tm.setValueAt(CourseTxt.getText(), i, 3);
            tm.setValueAt(PasswordTxt.getText(), i, 4);
        }
        else{
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_UpdateButActionPerformed

