    private void LoadStudButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoadStudButActionPerformed

        tm.setRowCount(0);
        try {
            BufferedReader br = new BufferedReader(new FileReader("studentrecord.txt"));
            String read = null;
            while ((read = br.readLine()) != null) {
                String[] split = read.split(";");
                for (int i = 0; i < split.length; i++) {
                    tm.addRow(split);
                    break;
                }
            }
            br.close();

        } catch (IOException ex) {
            Logger.getLogger(ViewStudentRec.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_LoadStudButActionPerformed

